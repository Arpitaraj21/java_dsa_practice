
import java.util.Scanner;

// 2. Write a Program in Java to Add two Numbers.

public class sumTwoNum {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        int b = sc.nextInt();

        int c = a + b;

        System.out.println("Sum of two numbers: " + c);

    }
}
